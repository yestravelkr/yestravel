/**
 * inject-definitions.js
 *
 * nestjs-trpc가 server.ts를 자동 생성할 때 상수 참조만 복사하고
 * 정의는 복사하지 않는 문제를 해결합니다.
 *
 * types/ 폴더와 utils.ts에서 export된 항목들을 자동으로 파싱해서
 * server.ts에서 사용되는 것만 import합니다.
 *
 * 사용법:
 * - node scripts/inject-definitions.js        # 1회 실행
 * - node scripts/inject-definitions.js --watch # 파일 감시 모드
 */

const fs = require('fs');
const path = require('path');

const SERVER_TS_PATH = path.join(__dirname, '../src/server.ts');
const TYPES_DIR = path.join(__dirname, '../src/types');
const UTILS_PATH = path.join(__dirname, '../src/utils.ts');

const INJECTION_MARKER = '// AUTO-INJECTED IMPORTS';

/**
 * TypeScript 파일에서 export된 항목들을 추출
 */
function extractExports(filePath) {
  if (!fs.existsSync(filePath)) return [];

  const content = fs.readFileSync(filePath, 'utf-8');
  const exports = [];

  // export const/function/class 패턴
  const patterns = [
    /export\s+const\s+(\w+)/g,
    /export\s+function\s+(\w+)/g,
    /export\s+class\s+(\w+)/g,
    /export\s+enum\s+(\w+)/g,
  ];

  for (const pattern of patterns) {
    let match;
    while ((match = pattern.exec(content)) !== null) {
      exports.push(match[1]);
    }
  }

  return exports;
}

/**
 * types/ 폴더의 모든 export 수집
 */
function getAllTypesExports() {
  const exports = [];

  if (!fs.existsSync(TYPES_DIR)) return exports;

  const files = fs.readdirSync(TYPES_DIR).filter(f => f.endsWith('.ts') && f !== 'index.ts');

  for (const file of files) {
    const filePath = path.join(TYPES_DIR, file);
    exports.push(...extractExports(filePath));
  }

  return exports;
}

/**
 * utils.ts의 모든 export 수집
 */
function getUtilsExports() {
  return extractExports(UTILS_PATH);
}

/**
 * server.ts에서 사용되는 변수 중 정의되지 않은 것들 찾기
 */
function findUndefinedReferences(serverContent, availableExports) {
  const used = [];

  for (const exportName of availableExports) {
    // 단어 경계로 검색 (import 문 제외)
    const regex = new RegExp(`(?<!import[^;]*|from\\s*['"][^'"]*|//[^\\n]*)\\b${exportName}\\b`, 'g');
    if (regex.test(serverContent)) {
      used.push(exportName);
    }
  }

  return used;
}

function injectImports() {
  if (!fs.existsSync(SERVER_TS_PATH)) {
    console.log('[inject] server.ts not found, skipping');
    return false;
  }

  let content = fs.readFileSync(SERVER_TS_PATH, 'utf-8');

  // 이미 주입되어 있으면 스킵
  if (content.includes(INJECTION_MARKER)) {
    return false;
  }

  // 사용 가능한 export들 수집
  const typesExports = getAllTypesExports();
  const utilsExports = getUtilsExports();

  // server.ts에서 실제 사용되는 것들만 필터
  const usedFromTypes = findUndefinedReferences(content, typesExports);
  const usedFromUtils = findUndefinedReferences(content, utilsExports);

  if (usedFromTypes.length === 0 && usedFromUtils.length === 0) {
    console.log('[inject] No imports needed');
    return false;
  }

  // import 문 생성
  let importsToAdd = `
// ============================================
// ${INJECTION_MARKER} (DO NOT EDIT)
// Generated by scripts/inject-definitions.js
// ============================================`;

  if (usedFromTypes.length > 0) {
    importsToAdd += `
import {
${usedFromTypes.map(e => `  ${e},`).join('\n')}
} from './types';`;
  }

  if (usedFromUtils.length > 0) {
    importsToAdd += `
import {
${usedFromUtils.map(e => `  ${e},`).join('\n')}
} from './utils';`;
  }

  importsToAdd += `
// ============================================
`;

  // zod import 다음에 주입
  const zodImportMatch = content.match(/import \{ z \} from ["']zod["'];?\n/);
  if (!zodImportMatch) {
    console.error('[inject] Could not find zod import in server.ts');
    return false;
  }

  const insertPoint = zodImportMatch.index + zodImportMatch[0].length;
  const before = content.substring(0, insertPoint);
  const after = content.substring(insertPoint);

  const newContent = before + importsToAdd + after;

  fs.writeFileSync(SERVER_TS_PATH, newContent, 'utf-8');

  console.log(`[inject] Injected imports:`);
  if (usedFromTypes.length > 0) {
    console.log(`  - from types: ${usedFromTypes.join(', ')}`);
  }
  if (usedFromUtils.length > 0) {
    console.log(`  - from utils: ${usedFromUtils.join(', ')}`);
  }

  return true;
}

// Watch 모드
function watchMode() {
  console.log('[watch] Watching server.ts for changes...');

  let debounceTimer = null;

  fs.watch(path.dirname(SERVER_TS_PATH), (eventType, filename) => {
    if (filename === 'server.ts') {
      if (debounceTimer) clearTimeout(debounceTimer);
      debounceTimer = setTimeout(() => {
        injectImports();
      }, 100);
    }
  });

  // 시작 시 1회 실행
  injectImports();
}

// CLI 처리
const args = process.argv.slice(2);
if (args.includes('--watch') || args.includes('-w')) {
  watchMode();
} else {
  injectImports();
}