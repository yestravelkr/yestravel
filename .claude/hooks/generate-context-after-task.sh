다음 형식으로 체크리스트 결과를 출력하세요:

📋 Context 생성 체크리스트:
- [ ] 새로 알게 된 도메인 지식: {YES/NO} - {있다면 무엇인지}
- [ ] 기존에 없는 개발 패턴 발견: {YES/NO} - {있다면 무엇인지}
- [ ] 문서화할 반복 작업 절차: {YES/NO} - {있다면 무엇인지}

→ 저장 필요: {YES/NO}
→ 저장 위치: {저장할 경로 또는 "없음"}

YES가 있다면 해당 내용을 저장하세요.
모두 NO라면 "저장할 Context 없음"으로 보고하세요.

